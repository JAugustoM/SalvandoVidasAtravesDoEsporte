CREATE TABLE IF NOT EXISTS public.turmas (
  id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  nome TEXT NOT NULL,
  faixa_etaria public.faixa_etaria NOT NULL,
  descricao TEXT NOT NULL,
  horario_inicio TIME NOT NULL,
  horario_fim TIME NOT NULL,
  ativo BOOLEAN NOT NULL DEFAULT FALSE,
  e_segunda BOOLEAN NOT NULL DEFAULT FALSE,
  e_terca BOOLEAN NOT NULL DEFAULT FALSE,
  e_quarta BOOLEAN NOT NULL DEFAULT FALSE,
  e_quinta BOOLEAN NOT NULL DEFAULT FALSE,
  e_sexta BOOLEAN NOT NULL DEFAULT FALSE,
  e_sabado BOOLEAN NOT NULL DEFAULT FALSE,
  e_domingo BOOLEAN NOT NULL DEFAULT FALSE
);

ALTER TABLE public.turmas ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Give full access to all authenticated users"
ON public.turmas
AS PERMISSIVE
FOR ALL
TO authenticated
USING (true)
WITH CHECK (true);